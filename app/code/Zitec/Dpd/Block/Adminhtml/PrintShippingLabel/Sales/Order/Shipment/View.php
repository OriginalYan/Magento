namespace Zitec\Dpd\Block\Adminhtml\PrintShippingLabel\Sales\Order\Shipment;

/**
 *
 * @category   Zitec
 * @package    Zitec_Dpd
 * @author     Zitec COM <magento@zitec.ro>
 */
class View extends \Magento\Shipping\Block\Adminhtml\View
{

    /**
     * @var \Magento\Backend\Helper\Data
     */
    protected $backendHelper;

    public function _construct()
    {
        $objectManager = \Magento\Framework\App\ObjectManager::getInstance();

        $this->registry = $objectManager->get(\Magento\Framework\Registry::class);
        $this->backendHelper = $objectManager->get(\Magento\Backend\Helper\Data::class);

        if ($this->_hasPdfShippingLabel()) {
            $this->addButton('zitec_print_shipping_label', [
              'label'   => __('Print Shipping Labels'),
              'onclick' => "setLocation('{$this->_getPrintShippingLabelsUrl()}')",
            ]);
        }

        return parent::_construct(); // TODO: Change the autogenerated stub
    }

    /**
     * @return bool
     */
    protected function _hasPdfShippingLabel()
    {
        return $this->getShipment()->getShippingLabel() ? true : false;
    }

    /**
     *
     * @return string
     */
    protected function _getPrintShippingLabelsUrl()
    {
        return $this->backendHelper->getUrl('zitec_printshippinglabel/adminhtml_index/printshippinglabels',
          ['shipment_id' => $this->getShipment()->getId()]);
    }
}
